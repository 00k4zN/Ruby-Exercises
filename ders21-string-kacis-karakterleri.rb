#Kaçış karakteri olarak kullanılan \ işareti, 
#bir sonraki karakterin özel bir karakter olduğunu belirtir. 
#Örneğin, \n karakter dizisi içinde bir satır sonu karakteri olduğunu belirtir. 
#Aşağıdaki tabloda, kaçış karakterlerinin listesi verilmiştir. 
puts 'Merhaba \'Efe\', bugun nasilsin?'

puts "Merhaba \"Efe\", bugun nasilsin?"

puts "Merhaba \nRuby"
puts "\tMerhaba Ruby"

